package memship;

public class MemShip {
	private int msNum;
	private int disc;	
	private String grade;
	private String id;
	
	public int getMsNum() {
		return msNum;
	}
	public void setMsNum(int msNum) {
		this.msNum = msNum;
	}
	public int getDisc() {
		return disc;
	}
	public void setDisc(int disc) {
		this.disc = disc;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
