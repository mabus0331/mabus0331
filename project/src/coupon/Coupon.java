package coupon;

public class Coupon {
	private String cpNum;
	private String cpName; 
	private int cpPer;
	
	public String getCpNum() {
		return cpNum;
	}
	public void setCpNum(String cpNum) {
		this.cpNum = cpNum;
	}
	public String getCpName() {
		return cpName;
	}
	public void setCpName(String cpName) {
		this.cpName = cpName;
	}
	public int getCpPer() {
		return cpPer;
	}
	public void setCpPer(int cpPer) {
		this.cpPer = cpPer;
	}
	
}
